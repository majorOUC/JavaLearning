package ouc.wangmengzhe.whileloop;

public class whileloopdemo {
    public static void main(String[] args) {
        //学习while循环
        whileloop();
    }

    public static void whileloop() {
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
    }

}
